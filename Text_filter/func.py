def camel_filter(a):
    up = True
    result = ''
    for i in a:
        if i == ' ':
            up = True
        elif up:
            up = False
            result += i.upper()
        else:
            result += i
    return result


def snake_filter(a):
    return a.replace(' ', '_')


def shouting_filter(a):
    return a.upper()


def whispering_filter(a):
    return a.lower()


def spacing_filter(a):
    delim = '.,;!?'
    for i in range(len(a)):
        if i != len(a) and a[i] in delim and a[i + 1] != ' ':
            a = a[:i + 1] + ' ' + a[i + 1:]
    return a


